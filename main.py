def get_all_urls():
	#PROXY = "127.0.0.1:8084" # IP:PORT or HOST:PORT
	chrome_options = webdriver.ChromeOptions("C:\Program Files (x86)\chromedriver.exe")
	#chrome_options.add_argument('--proxy-server=%s' % PROXY)
	#chrome_options.add_argument("user-data-dir=selenium")
	chrome_options.add_experimental_option("excludeSwitches",["ignore-certificate-errors"])
	chrome_options.add_argument('--disable-gpu')
	chrome_options.add_argument('--headless')
	chrome = webdriver.Chrome(chrome_options=chrome_options)
	chrome.get("https://www.instagram.com/kmt_art/")
	#src = chrome.page_source
	src = chrome.page_source
	urls = re.findall(URL_REGEX, src)
	while 'https://scontent-iad3-1.cdninstagram.com/vp/' not in str(urls):
		time.sleep(.5)
		src = chrome.page_source
		urls = re.findall(URL_REGEX, src)
		#print urls
	urlz = []
	prevVal = 0
	for i in range(100):
		chrome.execute_script("window.scrollTo(0, document.body.scrollHeight);")
		time.sleep(1)
		src = chrome.page_source
		urls = re.findall(URL_REGEX, src)
		for val in urls:
			if 's640x640' in val:
				if '"' in val:
					val = val.split('"')[0]
				if val not in urlz:
					urlz.append(val)
		if i % 3 == 0 and i != 0:
			if prevVal == len(urlz):
				return set(urlz)
			prevVal = len(urlz)

	return set(urlz)

if __name__ == '__main__':
	a = open("URLS.txt", "w")
